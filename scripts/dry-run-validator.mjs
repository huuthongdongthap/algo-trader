#!/usr/bin/env node
// Dry Run Validator — tracks 14-day paper trading progress toward live trading gate
// Reads daily-report markdown files from data/reports/ and validates criteria:
//   - 14+ days of paper trading
//   - 10+ profitable days
//   - Brier score < 0.20 (average)
//   - Win rate > 55% (aggregate)
//   - No single day loss > 5% of capital
//
// Usage: node scripts/dry-run-validator.mjs [--reports-dir path] [--capital amount]

import { existsSync, readdirSync, readFileSync } from 'node:fs';
import { resolve } from 'node:path';

// ── CLI args ────────────────────────────────────────────────────────────────

const args = process.argv.slice(2);

function getArg(flag, fallback) {
  const idx = args.indexOf(flag);
  return idx !== -1 && args[idx + 1] ? args[idx + 1] : fallback;
}

if (args.includes('--help') || args.includes('-h')) {
  console.log(`
Dry Run Validator — 14-day paper trading gate for live trading

Usage:
  node scripts/dry-run-validator.mjs [options]

Options:
  --reports-dir PATH   Directory containing daily-*.md reports (default: data/reports)
  --capital AMOUNT     Starting capital in USD (default: 200)
  --help, -h           Show this help

Criteria (ALL must pass for LIVE APPROVED):
  ✓ 14+ days of paper trading
  ✓ 10+ profitable days
  ✓ Average Brier score < 0.20
  ✓ Aggregate win rate > 55%
  ✓ No single day loss > 5% of capital
  ✓ 5-20 trades per day (avg)
`);
  process.exit(0);
}

const REPORTS_DIR = resolve(getArg('--reports-dir', 'data/reports'));
const CAPITAL = parseFloat(getArg('--capital', '200'));

// ── Parse daily reports ─────────────────────────────────────────────────────

/**
 * @typedef {{ date: string, pnl: number, winRate: number, brier: number, tradeCount: number, profitable: boolean }} DayResult
 */

function parseDailyReport(filePath) {
  const content = readFileSync(filePath, 'utf8');

  // Extract metrics from markdown format (generated by daily-report.mjs)
  const pnlMatch = content.match(/P&L[:\s]*\$?([-\d.]+)/i);
  const winRateMatch = content.match(/Win\s*Rate[:\s]*([\d.]+)%/i);
  const brierMatch = content.match(/Brier[:\s]*([\d.]+)/i);
  const tradesMatch = content.match(/Trades?[:\s]*(\d+)/i);
  const dateMatch = filePath.match(/daily-([\d-]+)\.md/);

  if (!dateMatch) return null;

  return {
    date: dateMatch[1],
    pnl: pnlMatch ? parseFloat(pnlMatch[1]) : 0,
    winRate: winRateMatch ? parseFloat(winRateMatch[1]) : 0,
    brier: brierMatch ? parseFloat(brierMatch[1]) : 1.0,
    tradeCount: tradesMatch ? parseInt(tradesMatch[1], 10) : 0,
    profitable: pnlMatch ? parseFloat(pnlMatch[1]) > 0 : false,
  };
}

// ── Main validation ─────────────────────────────────────────────────────────

function validate() {
  console.log('');
  console.log('  ╔══════════════════════════════════════════════╗');
  console.log('  ║    DRY RUN VALIDATION — Phase 1 Gate Check   ║');
  console.log('  ╚══════════════════════════════════════════════╝');
  console.log('');

  if (!existsSync(REPORTS_DIR)) {
    console.log(`  ❌ Reports directory not found: ${REPORTS_DIR}`);
    console.log('     Run paper trading first: npx tsx src/cli/index.ts paper');
    console.log('     Then generate daily reports: node scripts/daily-report.mjs');
    console.log('');
    process.exit(1);
  }

  const files = readdirSync(REPORTS_DIR)
    .filter(f => f.startsWith('daily-') && f.endsWith('.md'))
    .sort();

  if (files.length === 0) {
    console.log('  ❌ No daily reports found in', REPORTS_DIR);
    console.log('     Generate reports: node scripts/daily-report.mjs');
    process.exit(1);
  }

  const days = files
    .map(f => parseDailyReport(resolve(REPORTS_DIR, f)))
    .filter(d => d !== null);

  // ── Compute aggregates ──────────────────────────────────────────────────

  const totalDays = days.length;
  const profitableDays = days.filter(d => d.profitable).length;
  const avgBrier = days.reduce((s, d) => s + d.brier, 0) / totalDays;
  const totalTrades = days.reduce((s, d) => s + d.tradeCount, 0);
  const totalWins = days.reduce((s, d) => s + Math.round(d.tradeCount * d.winRate / 100), 0);
  const aggWinRate = totalTrades > 0 ? (totalWins / totalTrades) * 100 : 0;
  const avgTradesPerDay = totalTrades / totalDays;
  const totalPnl = days.reduce((s, d) => s + d.pnl, 0);
  const maxDayLoss = Math.min(...days.map(d => d.pnl));
  const maxDayLossPct = Math.abs(maxDayLoss) / CAPITAL * 100;

  // ── Check criteria ──────────────────────────────────────────────────────

  const checks = [
    { name: 'Days traded        ', target: '≥ 14', value: `${totalDays}`, pass: totalDays >= 14 },
    { name: 'Profitable days    ', target: '≥ 10', value: `${profitableDays}/${totalDays}`, pass: profitableDays >= 10 },
    { name: 'Avg Brier score    ', target: '< 0.20', value: avgBrier.toFixed(4), pass: avgBrier < 0.20 },
    { name: 'Aggregate win rate ', target: '> 55%', value: `${aggWinRate.toFixed(1)}%`, pass: aggWinRate > 55 },
    { name: 'Avg trades/day     ', target: '5-20', value: avgTradesPerDay.toFixed(1), pass: avgTradesPerDay >= 5 && avgTradesPerDay <= 20 },
    { name: 'Max single day loss', target: '< 5%', value: `${maxDayLossPct.toFixed(1)}% ($${maxDayLoss.toFixed(2)})`, pass: maxDayLossPct < 5 },
  ];

  const allPass = checks.every(c => c.pass);

  console.log('  ┌────────────────────────────────────────────────────────────┐');
  console.log('  │  Criteria              │ Target   │ Actual         │ Pass  │');
  console.log('  ├────────────────────────────────────────────────────────────┤');
  for (const c of checks) {
    const icon = c.pass ? '✅' : '❌';
    console.log(`  │  ${c.name}│ ${c.target.padEnd(9)}│ ${c.value.padEnd(15)}│ ${icon}    │`);
  }
  console.log('  └────────────────────────────────────────────────────────────┘');
  console.log('');
  console.log(`  Total P&L: $${totalPnl.toFixed(2)} over ${totalDays} days`);
  console.log(`  Capital  : $${CAPITAL} → $${(CAPITAL + totalPnl).toFixed(2)}`);
  console.log('');

  if (allPass) {
    console.log('  ╔══════════════════════════════════════════════╗');
    console.log('  ║   ✅  LIVE TRADING APPROVED — Tier 1 ($200)  ║');
    console.log('  ║                                              ║');
    console.log('  ║   Set PAPER_TRADING=false in .env to go live ║');
    console.log('  ║   Start: npx tsx src/cli/index.ts start      ║');
    console.log('  ╚══════════════════════════════════════════════╝');
  } else {
    const failing = checks.filter(c => !c.pass).length;
    const daysRemaining = Math.max(0, 14 - totalDays);
    console.log('  ╔══════════════════════════════════════════════╗');
    console.log(`  ║   ❌  NOT YET — ${failing} criteria failing            ║`);
    console.log('  ║                                              ║');
    if (daysRemaining > 0) {
      console.log(`  ║   ${daysRemaining} more days of paper trading needed     ║`);
    }
    console.log('  ║   Continue: npx tsx src/cli/index.ts paper    ║');
    console.log('  ╚══════════════════════════════════════════════╝');
  }
  console.log('');

  process.exit(allPass ? 0 : 1);
}

validate();
